import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.join(__dirname, '..')
const raw = fs.readFileSync(path.join(root, 'docs/ft6600-2025-12-17.txt'), 'utf8')
const lines = raw.split(/\n/)

const titleImportant = lines.slice(0, 18).join('\n')
// Lines 20–23: banner, blank line before body (matches prescribed layout)
const agreementHeader = lines.slice(19, 23).join('\n')
const clauses = lines.slice(23, 425).join('\n')
const notes = lines.slice(426, 460).join('\n')

function tsString(s) {
  return (
    '`' +
    s
      .replace(/\\/g, '\\\\')
      .replace(/`/g, '\\`')
      .replace(/\$\{/g, '\\${') +
    '`'
  )
}

const out = `// AUTO-GENERATED by scripts/extract-ft6600-for-tsx.mjs — do not edit by hand (source: src/lib/documents/ft6600EmbeddedStrings.ts)
export const FT6600_TITLE_AND_IMPORTANT = ${tsString(titleImportant)}

export const FT6600_AGREEMENT_HEADER = ${tsString(agreementHeader)}

export const FT6600_CLAUSES_1_TO_55 = ${tsString(clauses)}

export const FT6600_NOTES = ${tsString(notes)}
`
const outPath = path.join(root, 'src/lib/documents/ft6600EmbeddedStrings.ts')
fs.writeFileSync(outPath, out, 'utf8')
console.log('Wrote', outPath)
